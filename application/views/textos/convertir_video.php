<h2 class="title text-center">Convertir video a mp4</h2>
<p class="text2">Para convertir un video a mp4 siga los siguientes pasos:</p>
<p class="text2">
- Descargue, instale y ejecute el programa <a href="http://www.freemake.com/es/free_video_converter/">freemake.</a> <br />
- Busque los vídeos AVI que desea convertir a MP4 usando el botón "+Vídeo".<br />
- Seleccione formato MP4.<br />
- Haga clic en "Convertir" para obtener vídeos en MP4.
</p>
