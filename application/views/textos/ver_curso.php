<h2 class="title text-center">Ver un curso</h2>
<p class="text2"> Para ver un curso debes tener un usuario, y seguir los siguientes pasos:
<p class="text2">1. Loguearte a la plataforma con tu usuario.
<p class="text2">2. Buscar el curso que deseas mirar mediante el menu de categorias.
<p class="text2">3. Ingresar a la información del curso seleccionado.
<p class="text2">4. Si queres ver ese curso solo presiona ver, como alternativa podes agregarlo a favoritos para verlo después.