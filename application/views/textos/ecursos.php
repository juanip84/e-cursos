<h2 class="title text-center">Que es E-Cursos?</h2>
<p class="text2">
Es una plataforma online para capacitación interna en pymes.
</p>
<p class="text2"></p>
<p class="text2">Algunas de las funcionalidades son:</p>
<p class="text2">
- Subir y ver cursos por parte de todos los usuarios de la empresa.<br />
- Agregar cursos a favoritos para tenerlos a mano y verlos después.<br />
- Administración de los usuarios de la empresa (solo tiene acceso el administrador).<br />
- Asignar curso como obligatorio para ciertos usuarios y seguimiento de quien ya lo vio.<br />
- Carga de exámen asociado a un curso y seguimiento de resultados.<br />
- Centro de notificaciones donde se recibe el feedback del curso por parte de los que ya lo vieron.
</p>